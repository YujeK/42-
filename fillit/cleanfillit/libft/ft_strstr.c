#include "libft.h"

char	*ft_strstr(const char *str, const char *to_find)
{
	int i;
	int j;

	if (*to_find == '\0')
		return ((char*)str);
	i = -1;
	while (str[++i])
	{
		j = 0;
		while (str[i + j] && to_find[j] && str[i + j] == to_find[j])
			j++;
		if (!to_find[j])
			return ((char*)str + i);
	}
	return (NULL);
}
